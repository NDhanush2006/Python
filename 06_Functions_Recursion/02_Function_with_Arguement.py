# Function with Arguement
def name(name, ending): # functiion Definition(parameter)
    print("Good Day", name)
    print(ending)

# Positional Arguement
name("Dhanush", "Thank You ") #function call(Argument(pass value to variable or Parameter))
name("Roshan", "Thanks")

# Keyword arguement
name(name="Raj", ending="Ok")
name(ending="its",name="Ben")